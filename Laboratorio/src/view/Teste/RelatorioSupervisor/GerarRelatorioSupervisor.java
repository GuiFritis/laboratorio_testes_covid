    public void preparar(){
        preencheDias();
    }
    
    private void preencheDias(){
        try{          
            DefaultTableModel dft = (DefaultTableModel) tableDias.getModel();

            while(dft.getRowCount() > 0){
                dft.removeRow(0);
            }
            
            ArrayList<String[]> linhas = new DAOExame().getDias();
            
            for(String[] linha : linhas){
                dft.addRow(linha);
            }
                       
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de conex√£o com SQL");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro no driver");
        }
    }

