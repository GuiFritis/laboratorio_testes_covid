    public void preparar(){
        try{
            testes = new DAOExame().Invalidados();
            
            DefaultTableModel dtm = (DefaultTableModel) tableTestes.getModel();
            
            while(dtm.getRowCount() > 0){
                dtm.removeRow(0);
            }
            
            for(Exame exame : testes){
                String[] linha = new String[7];
                linha[0] = String.valueOf(exame.getCodExame());
                linha[1] = exame.getNome();
                linha[2] = String.valueOf(exame.getTeste().getNumTeste());
                linha[3] = String.valueOf(exame.getColeta().getCodColeta());
                linha[4] = exame.getDiaExame();
                linha[5] = exame.getColeta().getTipoMaterial();
                linha[6] = exame.getColeta().getPaciente().getCpf(); 
                
                dtm.addRow(linha);
            }          
                       
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de conex찾o com SQL");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro no driver");
        }
    }

