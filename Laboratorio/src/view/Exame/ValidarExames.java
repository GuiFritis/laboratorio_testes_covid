    private void validacao(int[] numTestes, boolean valido){
        
        try{
            DAOExame daot = new DAOExame();

            for(int i : numTestes){
                daot.validarExame(testes.get(i).getCodExame(), valido);
            }
            
            preparar();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro de conex찾o com SQL");
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(null, "Erro no driver");
        }    
        
    }
    
    
